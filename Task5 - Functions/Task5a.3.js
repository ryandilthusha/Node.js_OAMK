//Function to get Sum
function calSum(num1,num2)
{
    return num1+num2;
}

//********Funtion call 1st Time***************
//Call Function while saving the return value into a variable
let sum = calSum(2,3);
console.log(sum);   //Pint the variable

//********Funtion call 2nd Time***************
//Call Function while saving the return value into a variable
sum = calSum(2,2);
console.log(sum);   //Pint the variable

//********Funtion call 3rd Time***************
//Call Function while saving the return value into a variable
sum = calSum(2,-3);
console.log(sum);   //Pint the variable
