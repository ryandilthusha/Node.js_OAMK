//Function
function circleCircumference(radius)
{
    let cal = 2 * Math.PI * radius;
    console.log("Circumference of circle with radius " + radius + " is " + cal);
}   

//Function Call using variable value
let radius = 5;
circleCircumference(radius);

//Function Call using variable value
radius = 15;
circleCircumference(radius);